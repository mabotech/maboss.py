create table MT_T_CERTIFICATE (

    constraint   PK_CERTIFICATE primary key ( id ) 
);


CREATE SEQUENCE MT_SEQ_CERTIFICATE
  INCREMENT 1  MINVALUE 1 
  MAXVALUE 9223372036854775807  START 1000000
  CACHE 1; 

-------MT_INS_CERTIFICATE--------------------
CREATE OR REPLACE FUNCTION MT_INS_CERTIFICATE() RETURNS TRIGGER AS $BODY$ 
DECLARE ID INTEGER; 
BEGIN 
SELECT NEXTVAL('MT_SEQ_CERTIFICATE') INTO ID;
 NEW.ID := ID;
RETURN NEW;
END; 
$BODY$ LANGUAGE PLPGSQL; 
--------------

CREATE TRIGGER TG_MT_INS_CERTIFICATE
BEFORE INSERT ON MT_T_CERTIFICATE
FOR EACH ROW EXECUTE PROCEDURE MT_INS_CERTIFICATE(); 
-------------------END------------------------